import mongoose from "mongoose";

const { Schema } = mongoose;

const FormSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

export default mongoose.models.Form ?? mongoose.model("Cat", FormSchema);
